package com.belenot.mirea.schedule.model;

public class Subject {
    private int id;
    private String title;
    public int getId() {
	return id;
    }
    public Subject setId(int id) {
	this.id = id;
	return this;
    }
    public String getTitle() {
	return title;
    }
    public Subject setTitle(String title) {
	this.title = title;
	return this;
    }
    
}
